/**
 * Busca município pelo código IBGE
 */
export function getMunicipioByCode(codigo: string): MunicipioBA | undefined {
  return MUNICIPIOS_BA.find((m) => m.codigo === codigo);
}

/**
 * Busca municípios pelo nome (busca parcial, case-insensitive)
 */
export function searchMunicipios(query: string): MunicipioBA[] {
  const q = query.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
  return MUNICIPIOS_BA.filter((m) =>
    m.nome.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "").includes(q),
  );
}

